//Comparison Operators
 /* 
 
 === strict equal

 !== strict not equal

 > greater than
 
 >= greater than or equal

 < less than

 <= less than or equal 
 
 */

console.log(1 > 3)       // false
console.log(3 > 1)       // true
console.log(250 >= 250)  // true   
console.log(1 === 1)     // true
console.log(1 === 2)     // false
console.log(1 === '1');   // false
console.log(250 > 250) // false